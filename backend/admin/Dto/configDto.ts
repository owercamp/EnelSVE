class configDto {
  protected name: string;
  protected posSlice: number;
  protected idAument: number;
  protected cc: number;
  protected gatiso: number;
  protected ultimateAudio: number;

  constructor(name: string, posSlice: number, idAument: number, cc: number, gatiso: number, ultimateAudio: number) {
    this.name = name;
    this.posSlice = posSlice;
    this.idAument = idAument;
    this.cc = cc;
    this.gatiso = gatiso;
    this.ultimateAudio = ultimateAudio;
  }
}